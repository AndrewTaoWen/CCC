while True:
    txt = input()
    if txt == 'TTYL':
        print('talk to you later')
        break
    elif txt == 'CU':
        print('see you')
    elif txt == ':-)':
        print("I'm happy")
    elif txt == ';-)':
        print('wink')
    elif txt == ':-P':
        print('stick out my tongue')
    elif txt == '(~.~)':
        print('sleepy')
    elif txt == 'TA':
        print('totally awesome')
    elif txt == 'CCC':
        print('Canadian Computing Competition')
    elif txt == 'CUZ':
        print('because')
    elif txt == 'TY':
        print('thank-you')
    elif txt == 'YW':
        print("you're welcome")
    else:
        print(txt)
